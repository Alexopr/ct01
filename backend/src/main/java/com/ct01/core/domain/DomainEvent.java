package com.ct01.core.domain;

import java.time.LocalDateTime;

/**
 * Базовый интерфейс для всех доменных событий в системе.
 * Доменные события представляют важные изменения в домене,
 * о которых должны знать другие части системы.
 * 
 * Принципы доменных событий:
 * - Неизменяемость (immutable)
 * - Содержат информацию о том, что произошло
 * - Публикуются после успешного сохранения агрегата
 * - Используются для интеграции между агрегатами и bounded contexts
 */
public interface DomainEvent {
    
    /**
     * Получить время возникновения события
     * @return время события
     */
    LocalDateTime getOccurredAt();
    
    /**
     * Получить идентификатор агрегата, который сгенерировал событие
     * @return ID агрегата
     */
    Object getAggregateId();
    
    /**
     * Получить тип события (имя класса события)
     * @return тип события
     */
    default String getEventType() {
        return this.getClass().getSimpleName();
    }
    
    /**
     * Получить версию события для обратной совместимости
     * @return версия события
     */
    default int getVersion() {
        return 1;
    }
} 
