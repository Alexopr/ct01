package com.ct01.core.domain;

/**
 * Маркерный интерфейс для всех Entity в системе.
 * Entity - это объекты, которые имеют идентичность и жизненный цикл.
 * 
 * Принципы Entity:
 * - Имеют уникальную идентичность (ID)
 * - Равенство определяется по ID, а не по атрибутам
 * - Могут изменяться во времени (mutable)
 * - Инкапсулируют бизнес-правила и инварианты
 */
public interface Entity<ID> {
    
    /**
     * Получить уникальный идентификатор сущности
     * @return идентификатор сущности
     */
    ID getId();
    
    /**
     * Проверить инварианты сущности.
     * Должна вызываться после любых изменений состояния.
     */
    default void checkInvariants() {
        // Базовая реализация - можно переопределить в конкретных классах
    }
} 
